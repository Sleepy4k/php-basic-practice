/*
|--------------------------------------------------------------------------
| Operator Aritmatika
|--------------------------------------------------------------------------
|
| Jenis jenis operator aritmatika yaitu :
|   - + (penjumlahan)
|   - - (pengurangan)
|   - * (perkalian)
|   - / (pembagian)
|   - % (modulus)
|   - ++ (penambahan satu angka)
|   - -- (pengurangan satu angka)
|
| Note : penjelasan di bawah menggunakan variable dengan nilai berikut
|   A = 10
|   B = 20
|
| 1. penjumlahan
|   - Pengertian
|       Penjumlahan merupakan penambahan sekelompok bilangan atau lebih
|       menjadi suatu bilangan yang disebut jumlah
|
|   - Contoh
|       ^ A + B = 30
|
| 2. pengurangan
|   - Pengertian
|       Pengurangan yaitu operasi aritmetika yang mewakili operasi menghapus
|       objek dari koleksi. Pengurangan ditandai dengan tanda minus
|
|   - Contoh
|       ^ A - B = -10
|
| 3. perkalian
|   - Pengertian
|       Perkalian merupakan proses aritmatika dasar di mana satu bilangan
|       dilipatgandakan sesuai dengan bilangan pengalinya
|
|   - Contoh
|       ^ A * B = 200
|
| 4. pembagian
|   - Pengertian
|       pembagian merupakan proses aritmatika dasar di mana satu bilangan
|       dipecah rata menjadi bilangan yang lebih kecil sesuai dengan bilangan
|       pembaginya
|
|   - Contoh
|       ^ A / B = 2
|
| 5. modulus
|   - Pengertian
|       operasi modulus adalah sebuah operasi yang menghasilkan sisa pembagian
|       dari suatu bilangan terhadap bilangan lainnya
|
|   - Contoh
|       ^ B % A = 0
|
| 6. penambahan satu angka
|   - Pengertian
|       Yaitu operasi penambahan, yang mana nilai awal di tambah satu angka
|
|   - Contoh
|       ^ A++ = 11
|
| 7. pengurangan satu angka
|   - Pengertian
|       Yaitu operasi pengurangan, yang mana nilai awal di kurangi satu angka
|
|   - Contoh
|       ^ A-- = 9
|
*/

