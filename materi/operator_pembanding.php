/*
|--------------------------------------------------------------------------
| Operator Pembanding
|--------------------------------------------------------------------------
|
| Jenis jenis operator aritmatika yaitu :
|   - == (sama dengan)
|   - != (tidak sama dengan)
|   - > (lebih besar)
|   - < (lebih kecil)
|   - >= (lebih besar atau sama dengan)
|   - <= (lebih kecil atau sama dengan)
|
| Note : penjelasan di bawah menggunakan variable dengan nilai berikut
|   A = 10
|   B = 20
|
| 1. sama dengan
|   - Pengertian
|       Mengecek apakah data a bernilai sama dengan b
|
|   - Contoh
|       ^ A == B = false
|
| 2. tidak sama dengan
|   - Pengertian
|       Mengecek apakah data a bernilai tidak sama dengan b
|
|   - Contoh
|       ^ A != B = true
|
| 3. lebih besar
|   - Pengertian
|       Mengecek apakah data a bernilai lebih besar dari b
|
|   - Contoh
|       ^ A > B = false
|
| 4. lebih kecil
|   - Pengertian
|       Mengecek apakah data a bernilai lebih kecil dari b
|
|   - Contoh
|       ^ A < B = true
|
| 5. lebih besar atau sama dengan
|   - Pengertian
|       Mengecek apakah data a bernilai lebih besar atau sama dengan b
|
|   - Contoh
|       ^ A >= B = false
|
| 6. lebih kecil atau sama dengan
|   - Pengertian
|       Mengecek apakah data a bernilai lebih kecil atau sama dengan b
|
|   - Contoh
|       ^ A <= B = true
|
*/

