/*
|--------------------------------------------------------------------------
| Operator Logika
|--------------------------------------------------------------------------
|
| Jenis jenis operator aritmatika yaitu :
|   - and
|   - or
|   - &&
|   - ||
|   - !
|
| Note : penjelasan di bawah menggunakan variable dengan nilai berikut
|   A = 10
|   B = 20
|
| 1. and
|   - Pengertian
|       Memanggil operasi logika AND, jika kedua data bernilai true maka
|       hasil dari kondisi akan true
|
|   - Contoh
|       ^ (A and B) is true
|
| 2. or
|   - Pengertian
|       Memanggil operasi logika OR, jika salah satu data bernilai true maka
|       hasil dari kondisi akan true
|
|   - Contoh
|       ^ (A or B) is true
|
| 3. &&
|   - Pengertian
|       Memanggil operasi logika AND, jika kedua data bukan bernilai kosong maka
|       hasil dari kondisi akan true
|
|   - Contoh
|       ^ (A && B) is true
|
| 4. ||
|   - Pengertian
|       Memanggil operasi logika OR, jika salah satu data  bukan bernilai kosong
|       maka hasil dari kondisi akan true
|
|   - Contoh
|       ^ (A || B) is true
|
| 5. !
|   - Pengertian
|       Memanggil operasi logika NOT, digunakan untuk memutar balikan nilai data
|       jika data bernilai true maka operasi NOT akan mengubah menjadi false
|
|   - Contoh
|       ^ !(A and B) is false
|
*/

